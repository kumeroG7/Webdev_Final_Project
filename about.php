    <h1 class="heads" id="about-one">HISTORY</h1>
    <p class="paragraphs">The Elite Mentors is a nationwide non-gorvernmental and non-profitable organisation with access to almost every part of the country.
        It was started in December 11, 2014 by the former CEO of Majivuno Global Communications, Mr. Browns Thanji. Mr. 
        Thanji began by creating a random website blog, where he would post articles relating to differnt critical issues 
        in life like building habits, social intelligence and emotional intelligence.</p> <p class="paragraphs">Later, he started a YouTube podcast
        after his blog surpassed 100 thousand subscribers. As he continued expanding his client base, some of his good 
        friends noticed this and were interested in increasing the availability of such programs. That is when an 
        organisation was formed. Mr. Thanji and his team saw through the foundation of the organisation.</p> <p class="paragraphs">By November 2017,
        the organisation had began opening offices in several parts of the country to enable physical access by citizens in 
        need. Earlier in the year, the gorvernment through the Ministry of Public Affairs, Youth and Gender, noticed how fast
        the organisation has been growing and how it has significantly impacted the lives of many countrymen. As a result, they
        offered to partner with Elite Mentors, by providing resources, assets and subsidies to further propel the mission and
        vision of The Elite Mentors team.</p>

